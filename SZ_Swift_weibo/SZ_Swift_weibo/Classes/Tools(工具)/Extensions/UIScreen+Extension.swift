import UIKit

extension UIScreen {
        
    var screenWidth: CGFloat {
        
        return UIScreen.main.bounds.size.width
    }
    
    var screenHeight: CGFloat {
        
        return UIScreen.main.bounds.size.width
    }
    
    var screenScale: CGFloat {
        
        return UIScreen.main.scale
    }
}
